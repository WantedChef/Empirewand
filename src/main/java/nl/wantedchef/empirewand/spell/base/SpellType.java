package nl.wantedchef.empirewand.spell.base;

/**
 * Enum representing different types of spells in the EmpireWand plugin.
 */
public enum SpellType {
    /**
     * Basic lightning spells.
     */
    LIGHTNING,
    
    /**
     * Fire-based spells.
     */
    FIRE,
    
    /**
     * Life and healing spells.
     */
    LIFE,
    
    /**
     * Dark magic spells.
     */
    DARK,
    
    /**
     * Movement and teleportation spells.
     */
    MOVEMENT,
    
    /**
     * Control and utility spells.
     */
    CONTROL,
    
    /**
     * Weather manipulation spells.
     */
    WEATHER,
    
    /**
     * Enhanced or advanced spells.
     */
    ENHANCED,
    
    /**
     * Projectile-based spells.
     */
    PROJECTILE,
    
    /**
     * Miscellaneous utility spells.
     */
    MISC,
    
    /**
     * Poison and toxic spells.
     */
    POISON,
    
    /**
     * Ice and frost spells.
     */
    ICE,
    
    /**
     * Earth and terrain spells.
     */
    EARTH,
    
    /**
     * Elemental combination spells.
     */
    ELEMENTAL,
    
    /**
     * Healing spells.
     */
    HEAL,
    
    /**
     * Blood magic spells.
     */
    BLOOD,
    
    /**
     * Aura-based spells.
     */
    AURA
}
